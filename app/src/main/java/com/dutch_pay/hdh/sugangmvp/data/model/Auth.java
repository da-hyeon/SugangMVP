package com.dutch_pay.hdh.sugangmvp.data.model;

public class Auth {

    private String name;
    private String result;
    private String student_no;
    private String student_name;
    private String server_jsonp_addr;
    private String system_name;
    private String system_sys;
    private String system_version;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }

    public String getStudent_name() {
        return student_name;
    }

    public void setStudent_name(String student_name) {
        this.student_name = student_name;
    }

    public String getServer_jsonp_addr() {
        return server_jsonp_addr;
    }

    public void setServer_jsonp_addr(String server_jsonp_addr) {
        this.server_jsonp_addr = server_jsonp_addr;
    }

    public String getStudent_no() {
        return student_no;
    }

    public void setStudent_no(String student_no) {
        this.student_no = student_no;
    }

    public String getSystem_name() {
        return system_name;
    }

    public void setSystem_name(String system_name) {
        this.system_name = system_name;
    }

    public String getSystem_sys() {
        return system_sys;
    }

    public void setSystem_sys(String system_sys) {
        this.system_sys = system_sys;
    }

    public String getSystem_version() {
        return system_version;
    }

    public void setSystem_version(String system_version) {
        this.system_version = system_version;
    }

}
